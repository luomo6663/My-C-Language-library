//常量（constant）
/*
有些数据类型在程序使用之前已经预先设定
好了，在整个程序的运行过程中没有变化，
这些称为常量 （constant ）。
*/

//符号常量
/*
* 符号常量是用 #define 预处理指令定义的常量。
* 符号常量在程序中通常用大写字母表示，以便与变量区分开来。
* 符号常量在程序编译时被替换为其定义的值。
* 符号常量没有数据类型，它们只是文本替换。
* 符号常量不占用内存空间。
*/




//变量（variable）
/*
有些数据类型在程序运行期间可能会改变或被赋值，
这些称为变量（variable ）。
*/

//常变量
/*
const 关键字用于定义常变量。
常变量在程序运行过程中其值不能改变。
常变量在程序运行前必须被初始化。

*/

//常量与变量的区别
/*
* 常量在程序运行过程中其值不能改变，而变量的值可以改变。
* 常量在程序运行前必须被初始化，而变量可以在任何时候被赋值。
*/