
//使用C语言的7个步骤
/*
1.定义程序的目标：明确程序要解决的问题或实现的功能。

2.设计程序：规划程序的结构、算法、数据结构等，形成设计方案。

3.编写代码：根据设计，用编程语言（如 C、Python 等）实现具体代码。

4.编译：将源代码转换为计算机可执行的机器码（针对编译型语言）。

5.运行程序：执行编译后的程序，初步验证功能。

6.测试和调试程序：检测程序的正确性，排查并修复错误（调试）。

7.维护和修改程序：根据用户反馈、需求变化或新的 bug，对程序进行更新和优化。
*/

//运行机制
/*
编译器把源代码转换成中间代码，链接器把中间代码和其他代码合并，生成可执行文件。

源代码 → 编译器 → 目标代码文件 → 链接器（+ 启动代码 + 库函数） → 可执行文件

编译阶段：编译器将源代码文件（.c）转换为目标代码文件（如.obj/.o）。
			该文件包含源代码对应的机器语言指令，但不完【缺失程序运行所需的启动代码（程序与操作系统的接口）和库函数代码（如printf()的实现）】。

链接阶段：链接器将以下三部分合并，生成可执行文件（如.exe）：
		1.目标代码文件（用户代码的机器语言）；
		2.系统提供的启动代码（适配操作系统，如 Windows/Linux 的启动逻辑差异）；
		3.从库文件中提取的所需函数代码（如 C 标准库中printf()的实际实现）。

库的作用与模块化优势：库是预编译的函数集合（如 C 标准库），存储大量可复用的功能（如输入输出、数学运算）。
					  链接器仅提取程序实际用到的库函数代码，避免冗余。
					  同时，C 支持多模块独立编译（多个.c文件分别编译为目标文件），修改单个模块时只需重新编译该模块，再通过链接器合并，大幅提升开发效率。
*/